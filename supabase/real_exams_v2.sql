    'space', jsonb_build_object(
      'id', target_space.id,
      'slug', target_space.slug,
      'name', target_space.name,
      'groups', coalesce((
        select jsonb_agg(group_row.name order by group_row.name)
        from public.groups group_row
        where group_row.space_id = target_space.id
      ), '[]'::jsonb)
    ),
    'questions', coalesce((
      select jsonb_agg(jsonb_build_object(
