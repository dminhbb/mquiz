create or replace function public.assign_question_code()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.question_code is null then
    new.question_code := public.allocate_question_code(new.id);
  else
    insert into public.question_code_registry(code, question_id)
    values (new.question_code, new.id)
    on conflict (code) do nothing;
    if not found then
      raise exception 'Mã câu hỏi % đã được sử dụng.', new.question_code;
    end if;
  end if;
  return new;
end;
$$;

