create or replace function public.allocate_question_code(target_question_id bigint)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  candidate integer;
  inserted_count integer;
begin
  for attempt in 1..500 loop
    candidate := 10000000 + floor(random() * 90000000)::integer;
    insert into public.question_code_registry(code, question_id)
    values (candidate, target_question_id)
    on conflict do nothing;
    get diagnostics inserted_count = row_count;
    if inserted_count = 1 then
      return candidate;
    end if;
  end loop;
  raise exception 'Không thể cấp mã câu hỏi duy nhất. Hãy thử lại.';
end;
$$;

do $$
declare
  question_row record;
  allocated_code integer;
begin
  for question_row in
    select q.id, q.question_code
    from public.questions q
    order by q.id
  loop
    if question_row.question_code is null then
      allocated_code := public.allocate_question_code(question_row.id);
      update public.questions
      set question_code = allocated_code
      where id = question_row.id;
    else
      insert into public.question_code_registry(code, question_id)
      values (question_row.question_code, question_row.id)
      on conflict (code) do update set question_id = excluded.question_id;
    end if;
  end loop;
end;
$$;

