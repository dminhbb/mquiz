create or replace function public.attach_real_exam_metadata()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_max_attempts integer;
  target_ended_at timestamptz;
begin
  if new.mode = 'real' and new.real_exam_code is not null then
    select
      exam.id,
      exam.code,
      exam.legacy_version,
      exam.name,
      exam.start_at,
      exam.end_at,
      exam.max_attempts,
      exam.ended_at
    into
      new.real_exam_id,
      new.real_exam_code,
      new.real_exam_version,
      new.real_exam_name,
      new.real_exam_start_at,
      new.real_exam_end_at,
      target_max_attempts,
      target_ended_at
    from public.real_exams exam
    where exam.code = new.real_exam_code
      and exam.hidden_at is null;
    if new.real_exam_id is null then
      raise exception 'Đợt thi thật không tồn tại.';
    end if;
    if new.started_at < new.real_exam_start_at
       or new.started_at > new.real_exam_end_at
       or (target_ended_at is not null and new.started_at > target_ended_at) then
      raise exception 'Lượt thi không bắt đầu trong thời gian Đợt thi thật.';
    end if;
    if (
      select count(*)
      from public.quiz_attempts attempt
      where attempt.real_exam_id = new.real_exam_id
        and attempt.student_name_key = new.student_name_key
    ) >= target_max_attempts then
      raise exception 'Học viên đã hết số lượt Thi thật.';
    end if;
  elsif new.mode = 'real' then
    select s.real_exam_version, s.real_exam_name, s.real_start_at, s.real_end_at
    into new.real_exam_version, new.real_exam_name, new.real_exam_start_at, new.real_exam_end_at
    from public.spaces s
    where s.slug = new.space_slug;
  else
    new.real_exam_id := null;
    new.real_exam_code := null;
    new.real_exam_version := null;
    new.real_exam_name := null;
    new.real_exam_start_at := null;
    new.real_exam_end_at := null;
  end if;
  return new;
end;
$$;

drop trigger if exists quiz_attempts_attach_real_exam_metadata on public.quiz_attempts;
create trigger quiz_attempts_attach_real_exam_metadata
before insert on public.quiz_attempts
for each row execute function public.attach_real_exam_metadata();

do $$
declare
  legacy_row record;
  allocated_code integer;
  created_exam_id bigint;
begin
  for legacy_row in
    select
      qa.space_slug,
      qa.real_exam_version,
      max(qa.real_exam_name) as real_exam_name,
      min(qa.real_exam_start_at) as start_at,
      max(qa.real_exam_end_at) as end_at,
      max(s.id) as space_id
    from public.quiz_attempts qa
    join public.spaces s on s.slug = qa.space_slug
    where qa.mode = 'real'
      and qa.real_exam_version is not null
      and not exists (
        select 1 from public.real_exams exam
        where exam.legacy_version = qa.real_exam_version
      )
    group by qa.space_slug, qa.real_exam_version
  loop
    allocated_code := public.allocate_real_exam_code();
    insert into public.real_exams(
      code, space_id, name, start_at, end_at, ended_at, legacy_version,
      scoring_method, created_at, updated_at
    )
    values (
      allocated_code,
      legacy_row.space_id,
      coalesce(nullif(legacy_row.real_exam_name, ''), 'Đợt thi thật lịch sử'),
      coalesce(legacy_row.start_at, now() - interval '1 hour'),
      greatest(
        coalesce(legacy_row.end_at, now()),
        coalesce(legacy_row.start_at, now() - interval '1 hour') + interval '1 minute'
      ),
      coalesce(legacy_row.end_at, now()),
      legacy_row.real_exam_version,
      2,
      now(),
      now()
    )
    returning id into created_exam_id;

    update public.quiz_attempts
    set real_exam_id = created_exam_id,
        real_exam_code = allocated_code
    where mode = 'real'
      and real_exam_version = legacy_row.real_exam_version
      and space_slug = legacy_row.space_slug;
  end loop;
end;
$$;

do $$
declare
  space_row record;
  target_exam_id bigint;
  allocated_code integer;
begin
  for space_row in
    select *
    from public.spaces
    where real_exam_enabled
      and real_start_at is not null
      and real_end_at is not null
      and real_start_at < real_end_at
  loop
    select exam.id
    into target_exam_id
    from public.real_exams exam
    where exam.legacy_version = space_row.real_exam_version;

    if target_exam_id is null then
      allocated_code := public.allocate_real_exam_code();
      insert into public.real_exams(
        code, space_id, name, start_at, end_at, question_percent, timer_seconds,
        multi_percent, max_attempts, scoring_method, legacy_version
      )
      values (
        allocated_code,
        space_row.id,
        coalesce(nullif(space_row.real_exam_name, ''), 'Đợt thi thật đã chuyển đổi'),
        space_row.real_start_at,
        space_row.real_end_at,
        space_row.real_question_percent,
        space_row.real_timer_seconds,
        space_row.real_multi_percent,
        space_row.real_max_attempts,
        coalesce(space_row.real_scoring_method, 2),
        space_row.real_exam_version
      )
      returning id into target_exam_id;
    else
      update public.real_exams
      set name = coalesce(nullif(space_row.real_exam_name, ''), name),
          start_at = space_row.real_start_at,
          end_at = space_row.real_end_at,
          ended_at = case when now() > space_row.real_end_at then coalesce(ended_at, space_row.real_end_at) else null end,
          question_percent = space_row.real_question_percent,
          timer_seconds = space_row.real_timer_seconds,
          multi_percent = space_row.real_multi_percent,
          max_attempts = space_row.real_max_attempts,
          scoring_method = coalesce(space_row.real_scoring_method, 2),
          updated_at = now()
      where id = target_exam_id;
    end if;

    begin
      perform public.replace_real_exam_sources(target_exam_id, space_row.real_question_sets);
      perform public.generate_real_exam_snapshot_unchecked(target_exam_id);
    exception when others then
      raise notice 'Không thể tạo snapshot cho Space %: %', space_row.slug, sqlerrm;
    end;
  end loop;
end;
$$;

create or replace function public.cleanup_space_results_unchecked(target_space_id bigint)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_slug text;
  mock_days integer;
  deleted_mock_by_days integer := 0;
  deleted_mock_by_cap integer := 0;
begin
  select s.slug, coalesce(s.mock_result_retention_days, 7)
  into target_slug, mock_days
  from public.spaces s
  where s.id = target_space_id;

  if target_slug is null then
    raise exception 'Space không tồn tại.';
  end if;

  delete from public.quiz_attempts attempt
  where attempt.space_slug = target_slug
    and attempt.mode = 'mock'
    and attempt.submitted_at < now() - make_interval(days => mock_days);
  get diagnostics deleted_mock_by_days = row_count;

  with old_mock as (
    select attempt.id
    from public.quiz_attempts attempt
    where attempt.space_slug = target_slug and attempt.mode = 'mock'
    order by attempt.submitted_at desc
    offset 500
  )
  delete from public.quiz_attempts attempt
  using old_mock
  where attempt.id = old_mock.id;
  get diagnostics deleted_mock_by_cap = row_count;

  return jsonb_build_object(
    'ok', true,
    'mock_deleted_by_days', deleted_mock_by_days,
    'mock_deleted_by_cap', deleted_mock_by_cap,
    'real_deleted_by_exams', 0,
    'real_deleted_by_cap', 0
  );
end;
$$;

