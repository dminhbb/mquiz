create or replace function public.list_real_exam_results(
  target_real_exam_id bigint,
  result_limit integer default 20,
  target_revision_id bigint default null
)
returns jsonb
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  target_exam public.real_exams%rowtype;
begin
  select *
  into target_exam
  from public.real_exams
  where id = target_real_exam_id;

  if not found or not public.can_manage_space(target_exam.space_id) then
    raise exception 'Không có quyền xem kết quả Đợt thi thật này.';
  end if;

  if target_revision_id is not null and not exists (
    select 1
    from public.real_exam_revisions revision
    where revision.id = target_revision_id
      and revision.real_exam_id = target_real_exam_id
  ) then
    raise exception 'Phiên bản Đợt thi không hợp lệ.';
  end if;

  return coalesce((
    select jsonb_agg(to_jsonb(result_row) order by result_row.submitted_at desc)
    from (
      select
        attempt.id,
        target_exam.code as real_exam_code,
        target_exam.name as real_exam_name,
        attempt.real_exam_revision_id,
        revision.revision_no as real_exam_revision_no,
        attempt.group_name,
        attempt.student_name,
        attempt.score,
        attempt.total_questions,
        attempt.correct_count,
        attempt.wrong_count,
        attempt.duration_seconds,
        attempt.started_at,
        attempt.submitted_at
      from public.quiz_attempts attempt
      left join public.real_exam_revisions revision
        on revision.id = attempt.real_exam_revision_id
      where attempt.real_exam_id = target_real_exam_id
        and (
          target_revision_id is null
          or attempt.real_exam_revision_id = target_revision_id
        )
      order by attempt.submitted_at desc
      limit least(5000, greatest(1, coalesce(result_limit, 20)))
    ) result_row
  ), '[]'::jsonb);
end;
$$;

create or replace function public.get_real_exam_attempt_count(
  requested_code integer,
  requested_student_name text
)
returns integer
language sql
stable
security definer
set search_path = public
as $$
  select count(*)::integer
  from public.quiz_attempts attempt
  join public.real_exams exam
    on exam.id = attempt.real_exam_id
  where exam.code = requested_code
    and attempt.mode = 'real'
    and attempt.real_exam_revision_id = exam.current_revision_id
    and attempt.student_name_key = lower(trim(regexp_replace(
      coalesce(requested_student_name, ''),
      '\s+',
      ' ',
      'g'
    )))
$$;

create or replace function public.attach_real_exam_metadata()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_max_attempts integer;
  target_ended_at timestamptz;
  target_manual_running boolean;
begin
  if new.mode = 'real' and new.real_exam_code is not null then
    select
      exam.id,
      exam.code,
      exam.current_revision_id,
      exam.legacy_version,
      exam.name,
      exam.start_at,
      exam.end_at,
      exam.max_attempts,
      exam.ended_at,
      exam.manual_running
    into
      new.real_exam_id,
      new.real_exam_code,
      new.real_exam_revision_id,
      new.real_exam_version,
      new.real_exam_name,
      new.real_exam_start_at,
      new.real_exam_end_at,
      target_max_attempts,
      target_ended_at,
      target_manual_running
    from public.real_exams exam
    where exam.code = new.real_exam_code
      and exam.hidden_at is null;

    if new.real_exam_id is null then
      raise exception 'Đợt thi thật không tồn tại.';
    end if;
    if not target_manual_running then
      raise exception 'Đợt thi đã tạm dừng.';
    end if;
    if new.started_at < new.real_exam_start_at
       or new.started_at > new.real_exam_end_at
       or (target_ended_at is not null and new.started_at > target_ended_at) then
      raise exception 'Lượt thi không bắt đầu trong thời gian Đợt thi thật.';
    end if;
    if (
      select count(*)
      from public.quiz_attempts attempt
      where attempt.real_exam_revision_id = new.real_exam_revision_id
        and attempt.student_name_key = new.student_name_key
    ) >= target_max_attempts then
      raise exception 'Học viên đã hết số lượt Thi thật của phiên bản hiện tại.';
    end if;
  elsif new.mode = 'real' then
    select
      space.real_exam_version,
      space.real_exam_name,
      space.real_start_at,
      space.real_end_at
    into
      new.real_exam_version,
      new.real_exam_name,
      new.real_exam_start_at,
      new.real_exam_end_at
    from public.spaces space
    where space.slug = new.space_slug;
  else
    new.real_exam_id := null;
    new.real_exam_code := null;
    new.real_exam_revision_id := null;
    new.real_exam_version := null;
    new.real_exam_name := null;
    new.real_exam_start_at := null;
    new.real_exam_end_at := null;
  end if;
  return new;
end;
$$;

drop function if exists public.hide_real_exam(bigint);

create or replace function public.hide_real_exam(
  target_real_exam_id bigint,
  confirmation_code integer
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_exam public.real_exams%rowtype;
begin
  select *
  into target_exam
  from public.real_exams
  where id = target_real_exam_id
  for update;

  if not found or not public.can_manage_space(target_exam.space_id) then
    raise exception 'Không có quyền quản lý Đợt thi thật này.';
  end if;
  if target_exam.ended_at is null and now() <= target_exam.end_at then
    raise exception 'Chỉ có thể ẩn Đợt thi thật đã kết thúc.';
  end if;
  if confirmation_code is distinct from target_exam.code then
    raise exception 'Mã xác nhận Đợt thi không chính xác.';
  end if;

  update public.real_exams
  set hidden_at = now(),
      hidden_by = auth.uid(),
      updated_at = now()
  where id = target_real_exam_id
  returning * into target_exam;

  return to_jsonb(target_exam);
end;
$$;

