create table if not exists public.real_exam_revisions (
  id bigint generated by default as identity primary key,
  real_exam_id bigint not null references public.real_exams(id) on delete restrict,
  revision_no integer not null check (revision_no > 0),
  start_at timestamptz not null,
  end_at timestamptz not null,
  question_percent integer not null check (question_percent in (30, 50, 70, 100)),
  timer_seconds integer not null check (timer_seconds in (45, 60, 90, 120)),
  multi_percent integer not null check (multi_percent in (30, 50, 70, 100)),
  max_attempts integer not null check (max_attempts between 1 and 5),
  scoring_method integer not null check (scoring_method in (1, 2)),
  source_version uuid not null,
  created_by uuid,
  created_at timestamptz not null default now(),
  unique (real_exam_id, revision_no),
  check (start_at < end_at)
);

create table if not exists public.real_exam_revision_sources (
  revision_id bigint not null references public.real_exam_revisions(id) on delete restrict,
  question_set_id bigint not null references public.question_sets(id) on delete restrict,
  question_set_name text not null,
  percent integer not null check (percent between 0 and 100),
  primary key (revision_id, question_set_id)
);

create table if not exists public.real_exam_revision_question_refs (
  revision_id bigint not null references public.real_exam_revisions(id) on delete restrict,
  question_code integer not null references public.questions(question_code) on delete restrict,
  generated_order integer not null,
  created_at timestamptz not null default now(),
  primary key (revision_id, question_code),
  unique (revision_id, generated_order)
);

