create or replace function public.get_real_exam_public(requested_code integer)
returns jsonb
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  target_exam public.real_exams%rowtype;
  target_space public.spaces%rowtype;
  revision_no integer;
begin
  select *
  into target_exam
  from public.real_exams
  where code = requested_code
    and hidden_at is null;

  if not found then
    return jsonb_build_object('exists', false);
  end if;

  select *
  into target_space
  from public.spaces
  where id = target_exam.space_id;

  if not target_space.published then
    return jsonb_build_object('exists', false);
  end if;

  select revision.revision_no
  into revision_no
  from public.real_exam_revisions revision
  where revision.id = target_exam.current_revision_id;

  return jsonb_build_object(
    'exists', true,
    'id', target_exam.id,
    'code', target_exam.code,
    'name', target_exam.name,
    'status', public.real_exam_status(target_exam),
    'start_at', target_exam.start_at,
    'end_at', target_exam.end_at,
    'manual_running', target_exam.manual_running,
    'timer_seconds', target_exam.timer_seconds,
    'max_attempts', target_exam.max_attempts,
    'scoring_method', target_exam.scoring_method,
    'revision_id', target_exam.current_revision_id,
    'revision_no', revision_no,
    'space', jsonb_build_object(
      'id', target_space.id,
      'slug', target_space.slug,
      'name', target_space.name,
      'groups', coalesce((
        select jsonb_agg(group_row.name order by group_row.name)
        from public.groups group_row
        where group_row.space_id = target_space.id
      ), '[]'::jsonb)
    ),
    'questions', coalesce((
      select jsonb_agg(jsonb_build_object(
        'id', question.question_code,
        'question_code', question.question_code,
        'type', question.type,
        'content', question.content,
        'options_json', question.options_json,
        'order_no', ref.generated_order
      ) order by ref.generated_order)
      from public.real_exam_question_refs ref
      join public.questions question
        on question.question_code = ref.question_code
      where ref.real_exam_id = target_exam.id
        and public.real_exam_status(target_exam) = 'active'
    ), '[]'::jsonb)
  );
end;
$$;

