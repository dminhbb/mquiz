create or replace function public.real_exam_status(exam public.real_exams)
returns text
language sql
stable
as $$
  select case
    when exam.hidden_at is not null then 'hidden'
    when exam.ended_at is not null or now() > exam.end_at then 'ended'
    when now() < exam.start_at then 'scheduled'
    when not exam.manual_running then 'paused'
    else 'active'
  end
$$;

create or replace function public.set_real_exam_running(
  target_real_exam_id bigint,
  should_run boolean
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_exam public.real_exams%rowtype;
begin
  select *
  into target_exam
  from public.real_exams
  where id = target_real_exam_id
  for update;

  if not found or not public.can_manage_space(target_exam.space_id) then
    raise exception 'Không có quyền quản lý Đợt thi thật này.';
  end if;
  if target_exam.hidden_at is not null then
    raise exception 'Đợt thi thật đã được ẩn.';
  end if;
  if coalesce(should_run, false) and target_exam.end_at <= now() then
    raise exception 'Hãy điều chỉnh thời gian kết thúc Đợt thi';
  end if;

  update public.real_exams
  set manual_running = coalesce(should_run, false),
      ended_at = case
        when coalesce(should_run, false) then null
        else ended_at
      end,
      updated_at = now()
  where id = target_real_exam_id
  returning * into target_exam;

  return to_jsonb(target_exam) || jsonb_build_object(
    'status', public.real_exam_status(target_exam)
  );
end;
$$;

create or replace function public.archive_real_exam_revision_unchecked(
  target_real_exam_id bigint,
  target_revision_id bigint
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  delete from public.real_exam_revision_question_refs
  where revision_id = target_revision_id;

  insert into public.real_exam_revision_question_refs(
    revision_id,
    question_code,
    generated_order,
    created_at
  )
  select
    target_revision_id,
    ref.question_code,
    ref.generated_order,
    ref.created_at
  from public.real_exam_question_refs ref
  where ref.real_exam_id = target_real_exam_id;
end;
$$;

create or replace function public.create_real_exam_revision_unchecked(target_real_exam_id bigint)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  target_exam public.real_exams%rowtype;
  next_revision_no integer;
  created_revision_id bigint;
begin
  select *
  into target_exam
  from public.real_exams
  where id = target_real_exam_id
  for update;

  if not found then
    raise exception 'Đợt thi thật không tồn tại.';
  end if;

  select coalesce(max(revision_no), 0) + 1
  into next_revision_no
  from public.real_exam_revisions
  where real_exam_id = target_real_exam_id;

  insert into public.real_exam_revisions(
    real_exam_id,
    revision_no,
    start_at,
    end_at,
    question_percent,
    timer_seconds,
    multi_percent,
    max_attempts,
    scoring_method,
    source_version,
    created_by
  )
  values (
    target_exam.id,
    next_revision_no,
    target_exam.start_at,
    target_exam.end_at,
    target_exam.question_percent,
    target_exam.timer_seconds,
    target_exam.multi_percent,
    target_exam.max_attempts,
    target_exam.scoring_method,
    target_exam.source_version,
    auth.uid()
  )
  returning id into created_revision_id;

  insert into public.real_exam_revision_sources(
    revision_id,
    question_set_id,
    question_set_name,
    percent
  )
  select
    created_revision_id,
    source.question_set_id,
    source.question_set_name,
    source.percent
  from public.real_exam_sources source
  where source.real_exam_id = target_real_exam_id;

  perform public.archive_real_exam_revision_unchecked(
    target_real_exam_id,
    created_revision_id
  );

  update public.real_exams
  set current_revision_id = created_revision_id,
      updated_at = now()
  where id = target_real_exam_id;

  return created_revision_id;
end;
$$;

do $$
declare
  exam_row record;
  revision_id bigint;
begin
  for exam_row in
    select exam.id, exam.current_revision_id
    from public.real_exams exam
    order by exam.id
  loop
    if exam_row.current_revision_id is null then
      select revision.id
      into revision_id
      from public.real_exam_revisions revision
      where revision.real_exam_id = exam_row.id
      order by revision.revision_no desc
      limit 1;

      if revision_id is null then
        revision_id := public.create_real_exam_revision_unchecked(exam_row.id);
      else
        update public.real_exams
        set current_revision_id = revision_id
        where id = exam_row.id;
        perform public.archive_real_exam_revision_unchecked(exam_row.id, revision_id);
      end if;
    end if;
  end loop;
end;
$$;

