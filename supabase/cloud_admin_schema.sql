create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  fullname text not null,
  role text not null check (role in ('superadmin', 'admin')),
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.spaces (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  timer_seconds integer not null default 60 check (timer_seconds > 0),
  exam_start_time time,
  allowed_late_minutes integer not null default 30 check (allowed_late_minutes > 0),
  real_exam_enabled boolean not null default false,
  real_question_percent integer not null default 50 check (real_question_percent in (30, 50, 70, 100)),
  real_timer_seconds integer not null default 60 check (real_timer_seconds in (45, 60, 90, 120)),
  real_multi_percent integer not null default 50 check (real_multi_percent in (30, 50, 70, 100)),
  real_max_attempts integer not null default 1 check (real_max_attempts between 1 and 5),
  real_scoring_method integer not null default 1 check (real_scoring_method in (1, 2)),
  real_exam_name text,
  real_question_sets jsonb not null default '[]'::jsonb,
  real_exam_version text not null default gen_random_uuid()::text,
  real_start_at timestamptz,
  real_end_at timestamptz,
  published boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (
    not real_exam_enabled
    or (real_start_at is not null and real_end_at is not null and real_start_at < real_end_at)
  )
);

alter table public.spaces
