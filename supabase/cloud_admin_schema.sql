create or replace function public.can_manage_space(target_space_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.is_superadmin() or exists (
    select 1
    from public.space_admins sa
    join public.profiles p on p.id = sa.user_id
    where sa.user_id = auth.uid()
      and sa.space_id = target_space_id
      and p.active
  );
$$;

