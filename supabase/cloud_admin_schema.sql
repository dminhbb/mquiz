create or replace function public.prevent_last_group_delete()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if exists (select 1 from public.spaces where id = old.space_id)
     and (select count(*) from public.groups where space_id = old.space_id) <= 1 then
    raise exception 'Space phải có ít nhất 1 Group';
  end if;
  return old;
end;
$$;

