where real_exam_version is null;

alter table public.spaces
alter column real_exam_version set default gen_random_uuid()::text,
alter column real_exam_version set not null;

create table if not exists public.space_admins (
  user_id uuid not null references public.profiles(id) on delete cascade,
  space_id bigint not null references public.spaces(id) on delete cascade,
  primary key (user_id, space_id)
);

create table if not exists public.groups (
  id bigint generated by default as identity primary key,
  space_id bigint not null references public.spaces(id) on delete cascade,
  name text not null,
  created_at timestamptz not null default now(),
  unique (space_id, name)
);

create table if not exists public.question_sets (
  id bigint generated by default as identity primary key,
  space_id bigint not null references public.spaces(id) on delete cascade,
  name text not null,
  created_at timestamptz not null default now(),
  unique (space_id, name)
);

create table if not exists public.questions (
  id bigint generated by default as identity primary key,
  space_id bigint not null references public.spaces(id) on delete cascade,
  question_set_id bigint references public.question_sets(id) on delete set null,
  order_no integer not null,
  type text not null check (type in ('single', 'multi')),
  content text not null,
  options_json jsonb not null,
  correct_json jsonb not null,
  created_at timestamptz not null default now(),
  unique (space_id, order_no)
);

create index if not exists questions_space_order_idx
on public.questions (space_id, order_no);

alter table public.questions
add column if not exists question_set_id bigint references public.question_sets(id) on delete set null;

create or replace function public.prevent_last_group_delete()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if exists (select 1 from public.spaces where id = old.space_id)
     and (select count(*) from public.groups where space_id = old.space_id) <= 1 then
    raise exception 'Space phải có ít nhất 1 Group';
  end if;
  return old;
end;
$$;

drop trigger if exists groups_keep_one on public.groups;
create trigger groups_keep_one
before delete on public.groups
for each row execute function public.prevent_last_group_delete();

create or replace function public.is_superadmin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid() and role = 'superadmin' and active
  );
$$;

create or replace function public.can_manage_space(target_space_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.is_superadmin() or exists (
    select 1
    from public.space_admins sa
    join public.profiles p on p.id = sa.user_id
    where sa.user_id = auth.uid()
      and sa.space_id = target_space_id
      and p.active
  );
$$;

revoke all on function public.is_superadmin() from public;
revoke all on function public.can_manage_space(bigint) from public;
grant execute on function public.is_superadmin() to authenticated;
grant execute on function public.can_manage_space(bigint) to authenticated;
grant execute on function public.can_manage_space(bigint) to anon;

create or replace function public.get_space_public_status(requested_slug text)
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
  select case
    when s.id is null then jsonb_build_object('exists', false, 'published', false)
    else jsonb_build_object('exists', true, 'published', s.published)
  end
  from (select 1) seed
  left join public.spaces s on s.slug = requested_slug
  limit 1;
$$;

revoke all on function public.get_space_public_status(text) from public;
grant execute on function public.get_space_public_status(text) to anon, authenticated;

